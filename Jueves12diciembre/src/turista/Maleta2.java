package turista;

public class Maleta2 {
	
	private String marca;
	private double peso;
	
	public Maleta2(String marca, double peso) {
		super();
		this.marca = marca;
		this.peso = peso;
	}
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	
}

